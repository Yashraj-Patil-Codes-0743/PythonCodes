#Basic tuple operator
#1.Membership operator
tuple1=(34,45,34,23,43,46,76)
print(34 in tuple1)
print(30 in tuple1)
print(34 not in tuple1)

#2.Concatenation operator
tuple1=(1,2,3,4)
tuple2=(5,6,7,8)
tup3=tuple1+tuple2
print(tup3)

#3.Multiplication operator
tup=(4,5,6,7)
tup2=tup*3
print(tup2)
